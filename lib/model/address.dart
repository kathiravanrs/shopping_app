class Address {
  String firstName;
  String lastName;
  String streetAddress;
  String city;
  String state;
  int zip;
  String phone;

  Address({
    required this.firstName,
    required this.lastName,
    required this.streetAddress,
    required this.city,
    required this.state,
    required this.zip,
    required this.phone,
  });
}
